void decending_sort(int *arr, int arr_count)
{
    for(int round=1; round<arr_count; round++)
    {
        for(int i=0; i<arr_count-round; i++)
        {
            if(arr[i] < arr[i+1])
            {
                arr[i] = arr[i] + arr[i+1];
                arr[i+1] = arr[i] - arr[i+1];
                arr[i] = arr[i] - arr[i+1];
            }
        }
    }
}

